async function testYtDlpEnhancedHeaders() {
    console.log('\nüîç Testing yt-dlp with enhanced headers...');
    
    const ytDlpPath = process.env.YT_DLP_PATH || (process.platform === 'win32' ? './yt-dlp-windows.exe' : './yt-dlp');
    
    if (!require('fs').existsSync(ytDlpPath)) {
        console.log('‚ùå yt-dlp not found, skipping test');
        return false;
    }
    
    try {
        const result = await new Promise((resolve, reject) => {
            const child = spawn(ytDlpPath, [
                '--dump-json',
                '--no-playlist',
                '--quiet',
                '--no-warnings',
                '--user-agent', 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',
                '--add-header', 'Accept:text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,image/apng,*/*;q=0.8',
                '--add-header', 'Accept-Language:en-US,en;q=0.9',
                '--add-header', 'DNT:1',
                '--add-header', 'Connection:keep-alive',
                '--no-check-certificates',
                '--prefer-insecure',
                'https://www.youtube.com/watch?v=dQw4w9WgXcQ'
            ]);
            
            let stdout = '';
            let stderr = '';
            
            child.stdout.on('data', (data) => {
                stdout += data.toString();
            });
            
            child.stderr.on('data', (data) => {
                stderr += data.toString();
            });
            
            child.on('close', (code) => {
                if (code === 0) {
                    resolve(stdout);
                } else {
                    reject(new Error(`yt-dlp exited with code ${code}. Stderr: ${stderr}`));
                }
            });
            
            child.on('error', (err) => {
                reject(new Error(`Failed to start yt-dlp process: ${err.message}`));
            });
        });
        
        console.log('‚úÖ yt-dlp with enhanced headers executed successfully');
        
        try {
            const videoInfo = JSON.parse(result);
            console.log('‚úÖ JSON parsing successful');
            console.log(`üìπ Video title: ${videoInfo.title || 'Unknown'}`);
            console.log(`‚è±Ô∏è Duration: ${videoInfo.duration || 'Unknown'} seconds`);
            console.log(`üìä Formats available: ${videoInfo.formats ? videoInfo.formats.length : 0}`);
            
            return true;
        } catch (parseError) {
            console.log('‚ö†Ô∏è JSON parsing failed, but command executed');
            return true;
        }
        
    } catch (error) {
        console.error('‚ùå yt-dlp enhanced headers test failed:', error.message);
        return false;
    }
}

